    <h1 class="text-2xl font-bold text-zinc-100">Política de Tratamiento de Datos Personales</h1>
    <p class="text-zinc-500 text-sm">Pa'l Trip · Vigente desde el 8 de mayo de 2026</p>
  </div>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">1. Responsable del tratamiento</h2>
    <p>
      El responsable del tratamiento de los datos personales recolectados a través de Pa'l Trip es el
      desarrollador o persona natural que opera esta aplicación. Para solicitudes relacionadas con tus
      datos puedes escribir a <span class="text-brand-400">hola@mamanche.com</span>.
    </p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">2. Datos que recolectamos</h2>
    <p>Al usar Pa'l Trip recolectamos únicamente los datos que ingresas voluntariamente:</p>
    <ul class="list-disc list-inside space-y-1 pl-2">
      <li><strong class="text-zinc-100">Nombre</strong> — para identificarte dentro de tu grupo de viaje.</li>
      <li>
        <strong class="text-zinc-100">Dato bancario (Bre-B / Nequi / Cuenta)</strong> — número de cuenta,
        teléfono Nequi u otro dato de pago que elijas compartir. Se usa exclusivamente para que los
        miembros del grupo sepan a dónde transferir dinero al momento de liquidar gastos.
      </li>
    </ul>
    <p>No recolectamos contraseñas, correos electrónicos, documentos de identidad ni datos de geolocalización.</p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">3. Finalidad del tratamiento</h2>
    <p>Tus datos se usan para:</p>
    <ul class="list-disc list-inside space-y-1 pl-2">
      <li>Identificarte dentro de un grupo de viaje específico.</li>
      <li>Calcular y mostrar el balance de gastos compartidos del grupo.</li>
      <li>Facilitar la liquidación de deudas entre los miembros del grupo (tu dato bancario se muestra a los demás miembros al momento de liquidar).</li>
    </ul>
    <p>No usamos tus datos para perfilado, publicidad, ni los compartimos con terceros distintos a los miembros del grupo de viaje al que te uniste.</p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">4. Base legal (Ley 1581 de 2012)</h2>
    <p>
      El tratamiento de tus datos se fundamenta en tu <strong class="text-zinc-100">consentimiento libre e
      informado</strong>, expresado al crear o unirte a un viaje en Pa'l Trip. Si no quieres que tus datos sean
      tratados, no crees ni te unas a ningún viaje.
    </p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">5. Compartición de datos</h2>
    <p>
      Tu nombre es visible para todos los miembros de los viajes a los que perteneces.
      Tu dato bancario (<em>Bre-B / Nequi / Cuenta</em>) solo se muestra a los miembros del grupo
      en la pantalla de liquidación, cuando alguien te debe dinero. No se comparte en ningún
      otro contexto dentro de la aplicación.
    </p>
    <p>
      Los datos se almacenan en servidores de <strong class="text-zinc-100">Supabase</strong> (infraestructura
      en la nube). Supabase actúa como encargado del tratamiento bajo sus propios términos de servicio y
      políticas de privacidad.
    </p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">6. Tus derechos como titular</h2>
    <p>De acuerdo con la Ley 1581 de 2012 y el Decreto 1377 de 2013, tienes derecho a:</p>
    <ul class="list-disc list-inside space-y-1 pl-2">
      <li><strong class="text-zinc-100">Conocer</strong> los datos que tenemos sobre ti.</li>
      <li><strong class="text-zinc-100">Actualizar</strong> tu nombre o dato bancario (próximamente desde la aplicación).</li>
      <li>
        <strong class="text-zinc-100">Suprimir</strong> tus datos: puedes salir de cualquier viaje en la
        pestaña <em>Miembros</em> → <em>Salir del viaje y eliminar mis datos</em>. Si no registraste gastos,
        tus datos se eliminan permanentemente. Si registraste gastos, tu nombre queda como
        "Usuario eliminado" y tu dato bancario se borra, para preservar la integridad del historial
        contable del grupo.
      </li>
      <li><strong class="text-zinc-100">Revocar</strong> el consentimiento en cualquier momento saliendo de todos los viajes.</li>
      <li><strong class="text-zinc-100">Presentar quejas</strong> ante la Superintendencia de Industria y Comercio (SIC) si consideras que tus derechos han sido vulnerados.</li>
    </ul>
    <p>Para ejercer cualquiera de estos derechos escríbenos a <span class="text-brand-400">hola@mamanche.com</span>.</p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">7. Tiempo de conservación</h2>
    <p>
      Tus datos se conservan mientras seas miembro de al menos un viaje activo en Pa'l Trip.
      Al salir del último viaje, tus datos personales identificables (nombre y dato bancario) se eliminan
      o anonimizan según lo descrito en la sección anterior. Los registros contables del grupo
      (montos, fechas, categorías) pueden conservarse para mantener la integridad del historial del viaje.
    </p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">8. Seguridad</h2>
    <p>
      Implementamos medidas técnicas razonables para proteger tus datos: comunicaciones cifradas (HTTPS),
      acceso a datos restringido por viaje, y tu dato bancario solo se expone en las consultas donde
      estrictamente se necesita. Sin embargo, ningún sistema en internet ofrece seguridad absoluta.
    </p>
  </section>

  <section class="space-y-3">
    <h2 class="text-lg font-semibold text-zinc-100">9. Cambios a esta política</h2>
    <p>
      Podemos actualizar esta política ocasionalmente. La versión vigente siempre estará disponible en
      <strong class="text-zinc-100">/privacidad</strong> con su fecha de entrada en vigor.
    </p>
  </section>

